public class LoginDetails
{
	private String username;
	private String password;
	
	public void setUsername(String input)
	{
		username = input;
	}
	
	public void setPassword(String input)
	{
		password = input;
	}
	
	public String getUsername()
	{
		return username;
	}
	
	public String getPassword()
	{
		return password;
	}
}
